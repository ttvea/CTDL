package Array;

public class Student {
	private String name;
	private int id;
	public Student(String name, int id) {
		this.name = name;
		this.id = id;
	}
	public String setName(String names) {
		return name=names;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	
}
